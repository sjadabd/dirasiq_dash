<script setup>
import { layoutConfig } from "@layouts";
import {
  VerticalNavGroup,
  VerticalNavLink,
  VerticalNavSectionTitle,
} from "@layouts/components";
import { useLayoutConfigStore } from "@layouts/stores/config";
import { injectionKeyIsVerticalNavHovered } from "@layouts/symbols";
import { computed, onMounted, ref } from "vue";
import { PerfectScrollbar } from "vue3-perfect-scrollbar";
import { VNodeRenderer } from "./VNodeRenderer";

const props = defineProps({
  tag: {
    type: null,
    required: false,
    default: "aside",
  },
  navItems: {
    type: null,
    required: true,
  },
  isOverlayNavActive: {
    type: Boolean,
    required: true,
  },
  toggleIsOverlayNavActive: {
    type: Function,
    required: true,
  },
});

const refNav = ref();
const isHovered = useElementHover(refNav);

provide(injectionKeyIsVerticalNavHovered, isHovered);

const configStore = useLayoutConfigStore();

// ŸÖÿ™ÿ∫Ÿäÿ±ÿßÿ™ ÿßŸÑŸÖÿ≥ÿ™ÿÆÿØŸÖ
const user = ref(null);
const userType = ref(null);

// ŸÅŸÑÿ™ÿ±ÿ© ÿßŸÑÿπŸÜÿßÿµÿ± ÿ≠ÿ≥ÿ® ŸÜŸàÿπ ÿßŸÑŸÖÿ≥ÿ™ÿÆÿØŸÖ
const filteredNavItems = computed(() => {
  if (!userType.value) {
    return [];
  }

  const isAllowed = (itemType) => {
    // ÿ•ÿ∞ÿß ŸÑŸÖ ŸäŸÉŸÜ ŸÑŸÑÿπŸÜÿµÿ± ŸÜŸàÿπÿå ŸÜÿπÿ±ÿ∂Ÿá ŸÑŸÑÿ¨ŸÖŸäÿπ
    if (!itemType) return true;

    // ÿ•ÿ∞ÿß ŸÉÿßŸÜ ŸÜŸàÿπ ÿßŸÑŸÖÿ≥ÿ™ÿÆÿØŸÖ Ÿäÿ∑ÿßÿ®ŸÇ ŸÜŸàÿπ ÿßŸÑÿπŸÜÿµÿ±
    if (itemType === userType.value) return true;

    return false;
  };

  // ŸÅŸÑÿ™ÿ±ÿ© ÿßŸÑÿπŸÜÿßÿµÿ± ŸÖÿπ ÿßŸÑÿ£ÿÆÿ∞ ÿ®ÿßŸÑÿ≠ÿ≥ÿ®ÿßŸÜ ÿßŸÑŸÖÿ¨ŸÖŸàÿπÿßÿ™ ŸàÿßŸÑÿπŸÜÿßŸàŸäŸÜ
  return props.navItems
    .map((item) => {
      // ÿ•ÿ∞ÿß ŸÉÿßŸÜ ÿπŸÜŸàÿßŸÜ ŸÇÿ≥ŸÖ (heading)ÿå ÿ£ÿ∏Ÿáÿ±Ÿá ÿØÿßÿ¶ŸÖŸãÿß
      if ("heading" in item) {
        return item;
      }

      // ÿ•ÿ∞ÿß ŸÉÿßŸÜ ŸÖÿ¨ŸÖŸàÿπÿ© ÿ™ÿ≠ÿ™ŸàŸä ÿπŸÑŸâ ÿπŸÜÿßÿµÿ± ŸÅÿ±ÿπŸäÿ©
      if ("children" in item) {
        const filteredChildren = item.children.filter((child) =>
          isAllowed(child.type)
        );
        if (filteredChildren.length > 0) {
          return { ...item, children: filteredChildren };
        }
        return null; // ÿ•ÿÆŸÅÿßÿ° ÿßŸÑŸÖÿ¨ŸÖŸàÿπÿ© ÿ•ŸÜ ŸÑŸÖ ŸäŸÉŸÜ ŸÅŸäŸáÿß ÿπŸÜÿßÿµÿ± ŸÖÿ≥ŸÖŸàÿ≠ ÿ®Ÿáÿß
      }

      // ÿπŸÜÿµÿ± ÿ±ÿßÿ®ÿ∑ ŸÖŸÅÿ±ÿØ
      return isAllowed(item.type) ? item : null;
    })
    .filter(Boolean); // ÿ•ÿ≤ÿßŸÑÿ© ÿßŸÑÿπŸÜÿßÿµÿ± null
});

// ÿ¨ŸÑÿ® ÿ®ŸäÿßŸÜÿßÿ™ ÿßŸÑŸÖÿ≥ÿ™ÿÆÿØŸÖ
const loadUserData = () => {
  const userData = localStorage.getItem("user");

  if (userData) {
    try {
      user.value = JSON.parse(userData);
      userType.value = user.value?.userType;
    } catch (error) {
      console.error("Error parsing user data:", error);
      user.value = null;
      userType.value = null;
    }
  } else {
    user.value = null;
    userType.value = null;
  }
};

onMounted(() => {
  loadUserData();

  // ÿßŸÑÿßÿ≥ÿ™ŸÖÿßÿπ ŸÑÿ™ÿ∫ŸäŸäÿ±ÿßÿ™ localStorage
  window.addEventListener("storage", (e) => {
    if (e.key === "user") {
      loadUserData();
    }
  });

  // ŸÖÿ±ÿßŸÇÿ®ÿ© ÿ™ÿ∫ŸäŸäÿ±ÿßÿ™ localStorage ŸÖŸÜ ŸÜŸÅÿ≥ ÿßŸÑÿ™ÿ®ŸàŸäÿ®
  const originalSetItem = localStorage.setItem;
  const originalRemoveItem = localStorage.removeItem;

  localStorage.setItem = function (key, value) {
    originalSetItem.apply(this, arguments);
    if (key === "user") {
      loadUserData();
    }
  };

  localStorage.removeItem = function (key) {
    originalRemoveItem.apply(this, arguments);
    if (key === "user") {
      loadUserData();
    }
  };
});

const resolveNavItemComponent = (item) => {
  if ("heading" in item) return VerticalNavSectionTitle;
  if ("children" in item) return VerticalNavGroup;

  return VerticalNavLink;
};

/*‚ÑπÔ∏è Close overlay side when route is changed
Close overlay vertical nav when link is clicked
*/
const route = useRoute();

watch(
  () => route.name,
  () => {
    props.toggleIsOverlayNavActive(false);
  }
);

const isVerticalNavScrolled = ref(false);
const updateIsVerticalNavScrolled = (val) =>
  (isVerticalNavScrolled.value = val);

const handleNavScroll = (evt) => {
  isVerticalNavScrolled.value = evt.target.scrollTop > 0;
};

const hideTitleAndIcon = configStore.isVerticalNavMini(isHovered);
</script>

<template>
  <Component
    :is="props.tag"
    ref="refNav"
    data-allow-mismatch
    class="layout-vertical-nav"
    :class="[
      {
        'overlay-nav': configStore.isLessThanOverlayNavBreakpoint,
        hovered: isHovered,
        visible: isOverlayNavActive,
        scrolled: isVerticalNavScrolled,
      },
    ]"
  >
    <!-- üëâ Header -->
    <div class="nav-header">
      <slot name="nav-header">
        <RouterLink to="/" class="app-logo app-title-wrapper">
          <VNodeRenderer :nodes="layoutConfig.app.logo" />

          <Transition name="vertical-nav-app-title">
            <h1
              v-show="!hideTitleAndIcon"
              class="app-logo-title leading-normal"
            >
              {{ layoutConfig.app.title }}
            </h1>
          </Transition>
        </RouterLink>
        <!-- üëâ Vertical nav actions -->
        <!-- Show toggle collapsible in >md and close button in <md -->
        <div class="header-action">
          <Component
            :is="layoutConfig.app.iconRenderer || 'div'"
            v-show="configStore.isVerticalNavCollapsed"
            class="d-none nav-unpin"
            :class="configStore.isVerticalNavCollapsed && 'd-lg-block'"
            v-bind="layoutConfig.icons.verticalNavUnPinned"
            @click="
              configStore.isVerticalNavCollapsed =
                !configStore.isVerticalNavCollapsed
            "
          />
          <Component
            :is="layoutConfig.app.iconRenderer || 'div'"
            v-show="!configStore.isVerticalNavCollapsed"
            class="d-none nav-pin"
            :class="!configStore.isVerticalNavCollapsed && 'd-lg-block'"
            v-bind="layoutConfig.icons.verticalNavPinned"
            @click="
              configStore.isVerticalNavCollapsed =
                !configStore.isVerticalNavCollapsed
            "
          />
          <Component
            :is="layoutConfig.app.iconRenderer || 'div'"
            class="d-lg-none"
            v-bind="layoutConfig.icons.close"
            @click="toggleIsOverlayNavActive(false)"
          />
        </div>
      </slot>
    </div>
    <slot name="before-nav-items">
      <div class="vertical-nav-items-shadow" />
    </slot>
    <slot
      name="nav-items"
      :update-is-vertical-nav-scrolled="updateIsVerticalNavScrolled"
    >
      <PerfectScrollbar
        :key="configStore.isAppRTL"
        tag="ul"
        class="nav-items"
        :options="{ wheelPropagation: false }"
        @ps-scroll-y="handleNavScroll"
      >
        <Component
          :is="resolveNavItemComponent(item)"
          v-for="(item, index) in filteredNavItems"
          :key="index"
          :item="item"
        />
      </PerfectScrollbar>
    </slot>

    <slot name="after-nav-items" />
  </Component>
</template>

